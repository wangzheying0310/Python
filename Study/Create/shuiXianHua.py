print("----------while-------------")
i = 100; r = 0; s = 0; t = 0
while i <1000:
    # // 地板除 除完之后的整数部分
    r = i // 100
    s = (i -r * 100) // 10
    t = i - r * 100 - s * 10
    # 幂运算 立方
    if i ==(r ** 3 + s ** 3 + t ** 3):
        print("i =",i)

    i += 1


print("----------for-------------")

i = 100; r = 0; s = 0; t = 0
for i in range(100,1000):
    r = i // 100
    s = (i - r * 100) // 10
    t = i - r * 100 - s * 10
    if i ==(r ** 3 + s ** 3 + t ** 3):
        print("i =",i)